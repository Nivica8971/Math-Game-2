float accelerate(float &speed, float &acceleration, float &deceleration, float &maxRunSpeed, float &maxWalkSpeed, boolean sprint) {
	if (sprint) {
		if (speed >= maxRunSpeed) {
			speed = maxRunSpeed;
		}
		else {
			speed += acceleration;
		}
	}
	else if (!sprint) {
		if (speed > maxWalkSpeed) {
			speed -= deceleration;
		}
		else {
			speed += acceleration; 
		}
	}
	return speed;
}

float decelerate(float& speed, float& acceleration, float& deceleration, float& maxRunSpeed, float& maxWalkSpeed, boolean sprint) {
	if (speed > 0) {
		speed -= deceleration;
	}
	else {
		speed = 0;
	}
	return speed;
}

void PhysicsPlayground::KeyboardHold()
{
	auto& player = ECS::GetComponent<PhysicsBody>(MainEntities::MainPlayer());
	b2Vec2 vel = b2Vec2(0.f, 0.f);

	float maxWalkSpeed = 20; 
	float maxRunSpeed = 40;
	boolean sprint = false;
	boolean move = false;
	float acceleration = 0.3;
	float deceleration = 0.5;

	if (Input::GetKey(Key::Shift))
	{
		sprint = true; 
	}

	if (Input::GetKeyDown(Key::Space))
	{

		vel += b2Vec2(0.f, 120.f);

	}
	if (Input::GetKey(Key::S))
	{
		vel += b2Vec2(0.f, -1.f);

	}
	if (Input::GetKey(Key::A))
	{
		move = true; 
		vel += b2Vec2(-1.f, 0.f);

	}
	if (Input::GetKey(Key::D))
	{
		move = true;
		vel += b2Vec2(1.f, 0.f);

	}
	if (move) {
		speed = accelerate(speed, acceleration, deceleration, maxRunSpeed, maxWalkSpeed, sprint);
	}
	else {
		speed = decelerate(speed, acceleration, deceleration, maxRunSpeed, maxWalkSpeed, sprint);
	}

	b2Vec2 newLinearVelocity = b2Vec2(
		speed * vel.x,
		player.GetBody()->GetLinearVelocity().y + vel.y);

	player.GetBody()->SetLinearVelocity(newLinearVelocity);

	std::cout << "Speed: " << speed << "\n";

	//player.GetBody()->SetLinearVelocity(speed * vel);
}

